INSERT INTO HorseOwner
(
HorseId,
FederationMemberId
)
SELECT
h.HorseId,
fm.FederationMemberId
FROM #HorseSeed s
JOIN Horse h
    ON h.FederationNumber = s.FederationNumber
CROSS APPLY
(
    SELECT TOP 1 p.PersonId
    FROM Person p
    WHERE
        -- התאמה מלאה
        s.OwnerName = p.FirstName + ' ' + p.LastName
        
        OR
        
        -- התאמה כמילים שלמות
        (
            ' ' + s.OwnerName + ' ' LIKE '% ' + p.FirstName + ' %'
        AND ' ' + s.OwnerName + ' ' LIKE '% ' + p.LastName + ' %'
        )
    ORDER BY
        CASE 
            WHEN s.OwnerName = p.FirstName + ' ' + p.LastName THEN 1
            ELSE 2
        END
) p
JOIN FederationMember fm
    ON fm.FederationMemberId = p.PersonId;