   PERSON MANAGED BY SYSTEM USER
   כל אדמין חווה מנהל את כל אנשי החווה שלו,
   כולל את עצמו,
   ולמעט מזכירות חווה מארחת
   ========================================================= */

INSERT INTO PersonManagedBySystemUser
(
    SystemUserId,
    PersonId,
    RequestDate,
    UpdateDate,
    ApprovalStatus
)
SELECT DISTINCT
    su.SystemUserId,
    pr.PersonId,
    SYSDATETIME(),
    SYSDATETIME(),
    N'Approved'
FROM PersonRanchRole adminRole
JOIN Role adminRoleDef
    ON adminRoleDef.RoleId = adminRole.RoleId
   AND adminRoleDef.RoleName = N'אדמין חווה'
JOIN SystemUser su
    ON su.SystemUserId = adminRole.PersonId
JOIN PersonRanch pr
    ON pr.RanchId = adminRole.RanchId
LEFT JOIN PersonRanchRole secretaryRole
    ON secretaryRole.PersonId = pr.PersonId
   AND secretaryRole.RanchId = pr.RanchId
LEFT JOIN Role secretaryRoleDef
    ON secretaryRoleDef.RoleId = secretaryRole.RoleId
   AND secretaryRoleDef.RoleName = N'מזכירת חווה מארחת'
WHERE secretaryRoleDef.RoleId IS NULL
  AND NOT EXISTS (
      SELECT 1
      FROM PersonManagedBySystemUser pm
      WHERE pm.SystemUserId = su.SystemUserId
        AND pm.PersonId = pr.PersonId
  );
GO
